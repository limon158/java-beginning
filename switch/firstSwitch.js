const color = 'red';
switch(color)
{
    case 'green':
        console.log('this is green');
        break;
    case 'yellow':
        console.log('this is yellow');
        break;
    case 'violet':
        console.log('this is violet');
        break;
    case 'red':
        console.log('this is red');
        break;
    case 'orange':
        console.log('this is orange');
        break;
    case 'white':
        console.log('this is white');
        break;
    default:
        console.log('this is black');
    
}